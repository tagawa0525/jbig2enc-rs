/// QMコーダの1状態エントリ。
///
/// `qe`: 確率推定値（LPS確率）
/// `mps`: MPS（より可能性の高いシンボル）出現時の次状態インデックス
/// `lps`: LPS（より可能性の低いシンボル）出現時の次状態インデックス
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub struct StateEntry {
    pub qe: u16,
    pub mps: u8,
    pub lps: u8,
}

const fn s(qe: u16, mps: u8, lps: u8) -> StateEntry {
    StateEntry { qe, mps, lps }
}

